    public static function isDisposable(string $email): bool
    {
        $email = strtolower(trim($email));
        $atPos = strrpos($email, '@');
        if ($atPos === false) {
            return false;
        }
        $domain = substr($email, $atPos + 1);
        if ($domain === '') {
            return false;
        }

        return in_array($domain, self::DOMAINS, true);
    }
